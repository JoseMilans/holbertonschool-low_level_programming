/**
 * reset_to_98 - Updates the value pointed to by a pointer to 98.
 * @n: Pointer to an integer.
 *
 * This function takes a pointer to an integer as a parameter and updates
 * the value it points to with the value 98. It modifies the value stored
 * at the memory location pointed to by the pointer and sets it to 98.
 */
void reset_to_98(int *n)
{
	*n = 98;
}

