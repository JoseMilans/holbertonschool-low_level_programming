/**
 * swap_int - Swaps the values of two integers.
 * @a: Pointer to the first integer.
 * @b: Pointer to the second integer.
 *
 * This function takes the addresses of two integers and swaps their values.
 * It dereferences the pointers to access the values stored at those addresses
 * and performs the swap operation to interchange the values.
 */
void swap_int(int *a, int *b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;
}
